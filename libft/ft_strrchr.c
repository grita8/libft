#include "libft.h"

char	*ft_strrchr(char *str, int c)
{
	int	i;

	c = (unsigned char)c;
	i = 0;
	while (str[i])
		i++;
	while (i >= 0)
	{
		if (str[i] == c)
			return (&str[i]);
		i--;
	}
	return (NULL);
}
